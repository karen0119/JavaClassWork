package class5;

public class E5LogicalOperators {
    public static void main(String[] args) {
// Logical Operator example --> NOT(!)

        boolean flag=true;
        System.out.println(!flag);
        // this will print a false
        System.out.println(flag);
        // this will print a true

        String name="Semir";
        // this will NOT print --> System.out.println(!name);
        //only works with boolean data type

        }


    }

