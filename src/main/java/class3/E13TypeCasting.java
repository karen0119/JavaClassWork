package class3;

public class E13TypeCasting {
    public static void main(String[] args) {

       byte num=127;
       short num1=num;
        System.out.println(num1);

        int num3=111112;
        double num4=num3;
        System.out.println(num4);

        float ca=10.5F;
        int ba= (int) ca;
        System.out.println(ba);



    }
}
