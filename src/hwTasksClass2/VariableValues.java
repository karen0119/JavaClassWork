package hwTasksClass2;

public class VariableValues {

    public static void main(String[] args) {
        byte number1=100;
        number1=101;
        System.out.println(number1);

        short number2=1_000;
        number2=1_001;
        System.out.println(number2);

        int number3=13;
        number3=14;
        System.out.println(number3);

        long number4=1_000_000_000L;
        number4=1_000_000_000_001L;
        System.out.println(number4);

        float number5=20.5F;
        number5=21.6F;
        System.out.println(number5);

        double number6=12.3;
        number6=13.1;
        System.out.println(number6);

        char gender='F';
        gender='M';
        System.out.println(gender);

        boolean isItSunny=true;
        isItSunny=false;
        System.out.println(isItSunny);
    }
}
